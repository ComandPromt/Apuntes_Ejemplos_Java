package Relacion1;

/** Clase Naturales100. Imprime los numeros naturales del 1 al 100.
 * @author David Martin
 * @version Version 1.0 07-10-2010
 */
public class Naturales1100 {

	/** Metodo main. Para imprimir valores numericos.
	 * @param args argumentos de la linea de comandos
	 */
	public static void main(String[] args) {
		// Mediante un bucle for se imprimen los numeros del 1 al 100
		for (int i=1;i<=100;i++){
			System.out.print(i+"\n");
		} //Fin for
	} //Fin main

} //Fin clase
